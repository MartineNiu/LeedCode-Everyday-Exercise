n = int(input())
count = 0
for i in range(1, n+1):
    sum = 0
    d = i%10
    while i > 0:
        sum+= i % 10
        i //= 10
    if sum%10 == d:
        count += 1
print(count)  # 输出满足条件的数字个数


# 📘 Python中的取模运算（%）规则总结

# ✅ 正数取模：
#     如果 a 和 b 都是正整数，那么 a % b 就是 a 除以 b 后的余数。
#     例如：5 % 2 = 1，因为 5 ÷ 2 = 2...1（余数为 1）

# ✅ 负数取模（被除数为负）：
#     如果 a 是负数，b 是正数，结果的符号与 b 相同（即为正）。
#     例如：-5 % 2 = 1，因为 -5 = (-3)*2 + 1

# ✅ 除数为负数：
#     如果 b 是负数，结果的符号与 b 相同（即为负）。
#     例如：5 % -2 = -1，因为 5 = (-2)*(-2) + (-1)

# 🧠 本质理解：
#     Python 的取模运算遵循：
#         a % b = a - b * (a // b)
#     其中 // 是地板除（floor division），向下取整。


